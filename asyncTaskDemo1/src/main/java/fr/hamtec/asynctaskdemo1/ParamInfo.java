package fr.hamtec.asynctaskdemo1;

public class ParamInfo {
    private String param1;
    private String param2;
    
    public ParamInfo(String param1, String param2)  {
        this.param1 = param1;
        this.param2 = param2;
    }
    
    public String getParam1() {
        return param1;
    }
    
    public String getParam2() {
        return param2;
    }
}
